          never be balanced. Imagine a card that does awesome things while being
          in the dust pile. You discard this card from your hand through another
          card, which is normally a negative effect, but in that case it might
          win you the game. Normally the card would be balanced by making it
          more expensive. We cannot do that, because its cost are never paid.
          This is considered an unbalanceable card. Marius has identified a
          couple of those cards and writes the grammar, which does not allow the
          creation of such cards. A grammar? Sounds like english class, well no,
          cards in collectible card games have abilities which follow rules.
          These abilities are written down according to a grammar. Marius
          defines this grammar and it is a hard job, because we want to have a
          card complexity similar to Magic the Gathering, but we cannot use the
          same grammar like Magic, because it creates unsolvable problems. In
          Magic these unsolvable problems are avoided by not printing
          conflicting cards, but here we cannot guarantee that, because you, the
          users, are allowed to create cards. And even in Magic the card
          creators should be aware, but still have failed sometimes, for example
          when the cards Humility and Opalescence came together. To solve this
          unsolvable Problem, the Magic judges decided to add more rules to the
          game. This is something we cannot do. And even if we could, it would
          not help, because Gödel's Incompleteness Theorem says that such
          problems, where you need to define a new rule to have a clear outcome,
          will never end. The only way out is to have a grammar, which do not
          have such undecidable things. If all of this just some crazy
          theoretical bla, then just keep in mind Marius is a nice guy,
          otherwise he is the one who defines this grammar.
        </p>
